using System.Text;
using System.Web;

namespace SeamQ.Generator.Formatters;

public class HtmlFormatter : IOutputFormatter
{
    public string Format => "html";

    public Task<string> FormatAsync(string markdownContent, string title, CancellationToken cancellationToken = default)
    {
        var htmlBody = ConvertMarkdownToHtml(markdownContent);
        var html = BuildHtmlDocument(title, htmlBody);
        return Task.FromResult(html);
    }

    public string GetFileExtension() => ".html";

    private static string BuildHtmlDocument(string title, string body)
    {
