    /// <summary>
    /// A lightweight markdown-to-HTML converter that handles the subset of markdown
    /// produced by the ICD sections: headings, tables, code blocks, inline code,
    /// bold, italic, and paragraphs.
    /// </summary>
    private static string ConvertMarkdownToHtml(string markdown)
    {
        var sb = new StringBuilder();
        var lines = markdown.Split('\n');
        var inCodeBlock = false;
        var inTable = false;
        var inParagraph = false;
        var tableRowIndex = 0;

        for (var i = 0; i < lines.Length; i++)
        {
            var line = lines[i];

            // Code blocks
            if (line.TrimStart().StartsWith("```"))
            {
                if (inCodeBlock)
                {
                    sb.AppendLine("</code></pre>");
                    inCodeBlock = false;
                }
                else
                {
                    if (inParagraph) { sb.AppendLine("</p>"); inParagraph = false; }
                    if (inTable) { sb.AppendLine("</tbody></table>"); inTable = false; }
                    sb.AppendLine("<pre><code>");
                    inCodeBlock = true;
                }
                continue;
            }

            if (inCodeBlock)
            {
                sb.AppendLine(HttpUtility.HtmlEncode(line));
                continue;
            }

            var trimmed = line.TrimStart();

            // Blank line
            if (string.IsNullOrWhiteSpace(trimmed))
            {
                if (inParagraph) { sb.AppendLine("</p>"); inParagraph = false; }
                if (inTable) { sb.AppendLine("</tbody></table>"); inTable = false; tableRowIndex = 0; }
                continue;
            }

            // Headings
            if (trimmed.StartsWith('#'))
            {
                if (inParagraph) { sb.AppendLine("</p>"); inParagraph = false; }
                if (inTable) { sb.AppendLine("</tbody></table>"); inTable = false; tableRowIndex = 0; }

                var level = 0;
                while (level < trimmed.Length && trimmed[level] == '#') level++;
                if (level >= 1 && level <= 6)
                {
                    var text = trimmed[level..].TrimStart();
                    sb.AppendLine($"<h{level}>{ProcessInline(text)}</h{level}>");
                    continue;
                }
            }

            // Table separator row (e.g., |---|---|)
            if (trimmed.StartsWith('|') && trimmed.Contains("---"))
            {
                // Skip the separator line; it's already handled by the header detection
                continue;
            }

            // Table rows
            if (trimmed.StartsWith('|') && trimmed.EndsWith('|'))
            {
                if (inParagraph) { sb.AppendLine("</p>"); inParagraph = false; }

                var cells = trimmed.Trim('|').Split('|');

                if (!inTable)
                {
                    sb.AppendLine("<table>");
                    sb.AppendLine("<thead><tr>");
                    foreach (var cell in cells)
                    {
                        sb.AppendLine($"<th>{ProcessInline(cell.Trim())}</th>");
                    }
                    sb.AppendLine("</tr></thead>");
                    sb.AppendLine("<tbody>");
                    inTable = true;
                    tableRowIndex = 0;
                }
                else
                {
                    tableRowIndex++;
                    sb.AppendLine("<tr>");
                    foreach (var cell in cells)
                    {
                        sb.AppendLine($"<td>{ProcessInline(cell.Trim())}</td>");
                    }
                    sb.AppendLine("</tr>");
                }
                continue;
            }

            // Close table if a non-table line appears
            if (inTable)
            {
                sb.AppendLine("</tbody></table>");
                inTable = false;
                tableRowIndex = 0;
            }

            // List items
            if (trimmed.StartsWith("- ") || trimmed.StartsWith("* "))
            {
                if (inParagraph) { sb.AppendLine("</p>"); inParagraph = false; }
                sb.AppendLine($"<li>{ProcessInline(trimmed[2..])}</li>");
                continue;
            }

            // Horizontal rule
            if (trimmed is "---" or "***" or "___")
            {
                if (inParagraph) { sb.AppendLine("</p>"); inParagraph = false; }
                sb.AppendLine("<hr />");
                continue;
            }

            // Paragraph text
            if (!inParagraph)
            {
                sb.Append("<p>");
                inParagraph = true;
            }
            else
            {
                sb.Append(' ');
            }
            sb.Append(ProcessInline(trimmed));
        }

        if (inParagraph) sb.AppendLine("</p>");
        if (inTable) sb.AppendLine("</tbody></table>");
        if (inCodeBlock) sb.AppendLine("</code></pre>");

        return sb.ToString();
    }

    /// <summary>
    /// Processes inline markdown: bold, italic, inline code.
    /// </summary>
    private static string ProcessInline(string text)
    {
        var sb = new StringBuilder();
        var i = 0;

        while (i < text.Length)
        {
            // Inline code
            if (text[i] == '`')
            {
                var end = text.IndexOf('`', i + 1);
                if (end > i)
                {
                    sb.Append($"<code>{HttpUtility.HtmlEncode(text[(i + 1)..end])}</code>");
                    i = end + 1;
                    continue;
                }
            }

            // Bold
            if (i + 1 < text.Length && text[i] == '*' && text[i + 1] == '*')
            {
                var end = text.IndexOf("**", i + 2, StringComparison.Ordinal);
                if (end > i)
                {
                    sb.Append($"<strong>{HttpUtility.HtmlEncode(text[(i + 2)..end])}</strong>");
                    i = end + 2;
                    continue;
                }
            }

            // Italic
            if (text[i] == '*')
            {
                var end = text.IndexOf('*', i + 1);
                if (end > i)
                {
                    sb.Append($"<em>{HttpUtility.HtmlEncode(text[(i + 1)..end])}</em>");
                    i = end + 1;
                    continue;
                }
            }

            sb.Append(HttpUtility.HtmlEncode(text[i].ToString()));
            i++;
        }

        return sb.ToString();
    }
}
